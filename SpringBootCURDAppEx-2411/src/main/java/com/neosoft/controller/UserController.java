package com.neosoft.controller;

import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.http.HttpStatus;
import org.springframework.http.ResponseEntity;
import org.springframework.security.authentication.AuthenticationManager;
import org.springframework.security.authentication.UsernamePasswordAuthenticationToken;
import org.springframework.web.bind.annotation.PostMapping;
import org.springframework.web.bind.annotation.RequestBody;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RestController;

import com.neosoft.entity.UserInformation;
import com.neosoft.entity.UserRequest;
import com.neosoft.entity.UserResponse;
import com.neosoft.service.IUserInfoService;
import com.neosoft.util.JwtUtil;

@RestController
@RequestMapping("/user")
public class UserController {
	
	@Autowired
	private IUserInfoService userInfoService;
	
	@Autowired
	private JwtUtil jwtUtil;
	
	@Autowired
	private AuthenticationManager authenticationManager;
	
	// 1. User Save Operation
	@PostMapping("/save")
	public ResponseEntity<String> saveUserInfo(@RequestBody UserInformation userInformation){
		Integer savedUserId = userInfoService.saveUserInfo(userInformation);
		return ResponseEntity.ok(new StringBuilder()
				.append("USER ")
				.append(savedUserId)
				.append(" SAVED SUCCESSFULLY!")
				.toString());
	}
	
	// 2. validate user and generate token
	@PostMapping("/login")
	public ResponseEntity<UserResponse> login(@RequestBody UserRequest request) {
		authenticationManager.authenticate(new UsernamePasswordAuthenticationToken(request.getUsername(), request.getPassword()));
		String token = jwtUtil.generateToken(request.getUsername());
		return new ResponseEntity<>(new UserResponse(token,"Generated by Akash!"),HttpStatus.OK);
	}
}
